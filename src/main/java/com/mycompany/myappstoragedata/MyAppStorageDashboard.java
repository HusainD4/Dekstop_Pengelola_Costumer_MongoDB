    private void loadDataToTable() {
        MongoDatabase database = connection.getDatabase();
        if (database == null) {
            JOptionPane.showMessageDialog(this, "Database tidak terhubung!");
            return;
        }

        var collection = database.getCollection("Costumers");
        MongoCursor<Document> cursor = collection.find().iterator();

        String[] columnNames = {"ID", "Name", "Gender", "Email", "Phone"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        TB_Data.setModel(model);

        int total = 0;
        int totalMale = 0;
        int totalFemale = 0;

        try {
            while (cursor.hasNext()) {
                Document doc = cursor.next();
                String id = doc.getObjectId("_id").toHexString();
                String name = doc.getString("name");
                String gender = doc.getString("gender");
                String email = doc.getString("email");
                String phone = doc.getString("phone");

                if ("laki-laki".equalsIgnoreCase(gender)) {
                    totalMale++;
                } else if ("perempuan".equalsIgnoreCase(gender)) {
                    totalFemale++;
                }

                model.addRow(new Object[]{id, name, gender, email, phone});
                total++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            cursor.close();
        }

        TOTAL_SELURUH_CUSTOMER.setText(String.valueOf(total));
        TOTAL_LAKI2.setText(String.valueOf(totalMale));
        TOTAL_PEREMPUAN.setText(String.valueOf(totalFemale));
    }

