    private void Input_DataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Input_DataActionPerformed
        String name = JOptionPane.showInputDialog("Masukkan nama customer:");
        String gender = JOptionPane.showInputDialog("Masukkan jenis kelamin (laki-laki/perempuan):");
        String email = JOptionPane.showInputDialog("Masukkan email:");
        String phone = JOptionPane.showInputDialog("Masukkan nomor HP:");

        if (name == null || gender == null || email == null || phone == null
                || name.isBlank() || gender.isBlank() || email.isBlank() || phone.isBlank()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }

        MongoDatabase database = connection.getDatabase();
        var collection = database.getCollection("Costumers");

        Document newCustomer = new Document("name", name)
                .append("gender", gender.toLowerCase())
                .append("email", email)
                .append("phone", phone);

        collection.insertOne(newCustomer);
        JOptionPane.showMessageDialog(this, "Data customer berhasil ditambahkan!");
        loadDataToTable();
    }//GEN-LAST:event_Input_DataActionPerformed

    private void Edit_DataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_DataActionPerformed
        int selectedRow = TB_Data.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diedit.");
            return;
        }

        String id = (String) TB_Data.getValueAt(selectedRow, 0);
        String currentName = (String) TB_Data.getValueAt(selectedRow, 1);
        String currentGender = (String) TB_Data.getValueAt(selectedRow, 2);
        String currentEmail = (String) TB_Data.getValueAt(selectedRow, 3);
        String currentPhone = (String) TB_Data.getValueAt(selectedRow, 4);

        String newName = JOptionPane.showInputDialog("Nama baru:", currentName);
        String newGender = JOptionPane.showInputDialog("Gender baru (laki-laki/perempuan):", currentGender);
        String newEmail = JOptionPane.showInputDialog("Email baru:", currentEmail);
        String newPhone = JOptionPane.showInputDialog("No HP baru:", currentPhone);

        MongoDatabase database = connection.getDatabase();
        var collection = database.getCollection("Costumers");

        Document updatedCustomer = new Document("name", newName)
                .append("gender", newGender.toLowerCase())
                .append("email", newEmail)
                .append("phone", newPhone);

        Document query = new Document("_id", new org.bson.types.ObjectId(id));
        collection.replaceOne(query, updatedCustomer);

        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
        loadDataToTable();
    }//GEN-LAST:event_Edit_DataActionPerformed

    private void Hapus_DataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Hapus_DataActionPerformed
        int selectedRow = TB_Data.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus.");
            return;
        }

        String idToDelete = (String) TB_Data.getValueAt(selectedRow, 0);
        MongoDatabase database = connection.getDatabase();
        var collection = database.getCollection("Costumers");

        Document query = new Document("_id", new org.bson.types.ObjectId(idToDelete));
        DeleteResult result = collection.deleteOne(query);

        if (result.getDeletedCount() > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan.");
        }
        loadDataToTable();
    }//GEN-LAST:event_Hapus_DataActionPerformed

